#pragma once

// Kilobytes
#define KB(num) (1024 * (num))

// Megabytes
#define MB(num) (1024 * KB(num))

// Gigabytes
#define GB(num) (1024 * MB(num))

// Terabytes
#define TB(num) (1024 * GB(num))

// Petabytes
#define PB(num) (1024 * TB(num))

// Exabytes
#define EB(num) (1024 * PB(num))

// Zettabytes
#define ZB(num) (1024 * EB(num))

// Yottabytes
#define YB(num) (1024 * ZB(num))
